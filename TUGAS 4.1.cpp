#include<iostream>
using namespace std;
int main()
{
	int n,a=1,rata,angka,jumlah;
	cout << "Masukan banyak bilangan : ";
	cin>> n;
	while(a<=n){
		cout<< "Masukan angka : ";
		cin>> angka;
		jumlah=jumlah+angka;
		a++;
	}
		rata=jumlah/n;
		cout<< "Rata-rata : " <<rata;
}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

